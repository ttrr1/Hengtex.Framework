using Hengtex.Application.Entity.BaseManage;
using Hengtex.Application.Busines.BaseManage;
using Hengtex.Util;
using Hengtex.Util.WebControl;
using System.Web.Mvc;

namespace Hengtex.Application.Web.Areas.BaseManage.Controllers
{
    /// <summary>
    /// 版 本 1.0
